    public void calctyreprice()
    {
         try
       {
            String taxfield=titax.getText();
            String pricefield=tiretpri.getText();
            String discountfield=tdiscount.getText();
            if(taxfield.isEmpty()||discountfield.isEmpty())
            {
                taxfield="0.0";
                discountfield="0.0";
            }
            double tax=Double.parseDouble(taxfield);
            double price=Double.parseDouble(pricefield);

            double discount=Double.parseDouble(discountfield);
            double priwithtaxanddiscount=(((tax/100.0)*price)-((discount/100.0)*price)+price);
            String s=String.valueOf(priwithtaxanddiscount);
            tiretpritax.setText(s);
       }
       catch(Exception e)
       {
       }
    }

